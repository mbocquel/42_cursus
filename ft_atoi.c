#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	n;
	int	i;
	int	sng;

	i = 0;
	sng = 1;
	n = 0;
	if (nptr[0] == '-')
	{
		sng = -1;
		i = 1;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		n = n * 10 + nptr[i] - '0';
		i++;
	}
	return (sng * n);
}
